package esisRepo.entity;

import androidx.room.Entity;
import androidx.room.PrimaryKey;

@Entity
public class State {
    @PrimaryKey
    private int id;

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getStatus() {
        return status;
    }

    public void setStatus(String status) {
        this.status = status;
    }

    private String status;
}
